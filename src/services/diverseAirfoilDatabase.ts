/**
 * Get airfoils by family
 */
export function getAirfoilsByFamily(family: AirfoilFamily): DiverseAirfoilData[] {
  return DIVERSE_AIRFOIL_DATABASE.filter(a => a.family === family);
}

/**
 * Get airfoils by category
 */
export function getAirfoilsByCategory(category: AirfoilCategory): DiverseAirfoilData[] {
  return DIVERSE_AIRFOIL_DATABASE.filter(a => a.category === category);
}

/**
 * Get random airfoils from different families
 */
export function getRandomDiverseAirfoils(count: number): DiverseAirfoilData[] {
  const families = Array.from(new Set(DIVERSE_AIRFOIL_DATABASE.map(a => a.family)));
  const selected: DiverseAirfoilData[] = [];
  
  // Ensure diversity by selecting from different families
  for (let i = 0; i < count && i < families.length; i++) {
    const family = families[i];
    const familyAirfoils = getAirfoilsByFamily(family);
    const randomIndex = Math.floor(Math.random() * familyAirfoils.length);
    selected.push(familyAirfoils[randomIndex]);
  }
  
  // Fill remaining slots with random airfoils
  while (selected.length < count) {
    const randomIndex = Math.floor(Math.random() * DIVERSE_AIRFOIL_DATABASE.length);
    const airfoil = DIVERSE_AIRFOIL_DATABASE[randomIndex];
    if (!selected.includes(airfoil)) {
      selected.push(airfoil);
    }
  }
  
  return selected;
}

/**
 * Get top performing airfoils
 */
export function getTopPerformingAirfoils(count: number): DiverseAirfoilData[] {
  return [...DIVERSE_AIRFOIL_DATABASE]
    .sort((a, b) => b.expectedLD - a.expectedLD)
    .slice(0, count);
}

/**
 * Get airfoils by performance range
 */
export function getAirfoilsByPerformance(minLD: number, maxLD: number): DiverseAirfoilData[] {
  return DIVERSE_AIRFOIL_DATABASE.filter(
    a => a.expectedLD >= minLD && a.expectedLD <= maxLD
  );
}
